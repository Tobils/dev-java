/**
 Program ini mendemonstrasikan loop while
 */

public class LoopWhile
{
   public static void main(String[] args)
   {
      int angka = 1;

      while (angka <= 5)
      {
         System.out.println("Halo");
         angka++;
      }

      System.out.println("Itu semuanya!");
   }
}